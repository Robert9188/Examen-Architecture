    public ArrayList<ModelsArticle> articleList(){
        ArrayList<ModelsArticle> articleList = new ArrayList<>();
    
       try{
           String driver = "com.mysql.cj.jdbc.Driver";  
           Class.forName(driver);
           String Url= "jdbc:mysql://localhost/javase";
           String User = "root";
           String Password = "";
           Connection c = DriverManager.getConnection(Url,User,Password);
           String queryl= "SELECT *FROM article";
           Statement st = c.createStatement();
           ResultSet rs= st.executeQuery(queryl);
           ModelsArticle article;
           while(rs.next()){
               article=new ModelsArticle(rs.getInt("id"),rs.getString("nom"),rs.getString("description"),rs.getString("contact"));
               articleList.add(article);
           }
       }catch(Exception e){
           JOptionPane.showConfirmDialog(null, e);
       }
       return articleList;
    }
    public void show_article(){
        ArrayList<ModelsArticle> list = articleList();
        
        DefaultTableModel model =(DefaultTableModel)jTable2.getModel();
        Object[] row = new Object [4];
        for(int i= 0; i<list.size();i++){
            row[0]=list.get(i).getId();
            row[1]=list.get(i).getNom();
            row[2]=list.get(i).getDescription();
            row[3]=list.get(i).getContact();
            model.addRow(row);
        }
    }

